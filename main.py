# 덧셈 (3교시)
a = 4
b = 2.0
total = a + b
print('total = a + b:', total)

# 뺄셈
total = a - b
print('total = a - b:', total)

# 곱셈
total = a * b
print('total = a * b:', total)

# 나눗셈
print('a / b:', a / b)

# 나머지
a = 5.5
b = 2.0
print('a%b: ', a % b)

# 거듭제곱
print('a ** b: ', a ** b)

# 몫 (양수)
print('a // b: ', a // b)

# 몫 (음수)
a = -5
print('a // b(음수) :', a // b)

# 변수 선언 (2교시)
